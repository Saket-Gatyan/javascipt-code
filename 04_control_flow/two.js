// switch 

const month = 'jan' ;

switch(month){
    case 'jan' :
        console.log("Jan");
        break;
    case 'feb' :
        console.log("feb");
        break;
    case 'mar':
        console.log("mar");
        break;
    case 'apr' :
        console.log("apr");
        break;
    case 'jun' :
        console.log("jun");
        break;
    default:
        console.log("kuch nhi bhai");
        break;
}